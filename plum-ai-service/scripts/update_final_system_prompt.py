def require_env(name: str) -> str:
    value = os.getenv(name)
    if not value:
        raise RuntimeError(f"Missing required environment variable: {name}")
    return value


def main() -> int:
    load_dotenv(AI_SERVICE_DIR / ".env")
    load_dotenv(PROJECT_ROOT / ".env")

    supabase = create_client(
        require_env("SUPABASE_URL"),
        require_env("SUPABASE_SERVICE_ROLE_KEY"),
    )

    update_response = (
        supabase.table("tenants")
        .update({"final_system_prompt": FINAL_SYSTEM_PROMPT})
        .eq("instance_id", INSTANCE_ID)
        .execute()
    )
    if not update_response.data:
        raise RuntimeError(f"No tenants row updated for instance_id={INSTANCE_ID!r}")

    verify_response = (
        supabase.table("tenants")
        .select("instance_id,final_system_prompt")
        .eq("instance_id", INSTANCE_ID)
        .limit(1)
        .execute()
    )
    row = verify_response.data[0] if verify_response.data else None
    saved_prompt = (row or {}).get("final_system_prompt")
    if saved_prompt != FINAL_SYSTEM_PROMPT:
        raise RuntimeError("Verification failed: saved final_system_prompt does not match expected text")

    digest = hashlib.sha256(saved_prompt.encode("utf-8")).hexdigest()[:16]
    print(
        "OK: public.tenants.final_system_prompt updated "
        f"for instance_id={INSTANCE_ID}; chars={len(saved_prompt)}; sha256={digest}"
    )
    return 0


if __name__ == "__main__":
    raise SystemExit(main())
